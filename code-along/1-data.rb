# In Ruby, there are different *types* of data:

# Numbers
puts 3
puts 5

puts 3+5 
puts 3/5
puts 3/5.000

# Non-numbers
puts "unicorn"

puts "3 + 5"
puts 3+5

# True, False

puts true
puts false 
# You run this more so as a check that something is true or false

# Nothing

# Variables

x = 3
y = 5
puts x
puts x+y

# You can reassign a variable at any time
y = 2
puts y

food = "Meat patties"
amount = 12
# Combine strings and variables

puts "Tacos on today's menu: #{x} dollars" 
puts "#{food} on today's menu: #{amount} Euros"
# String manipulation
puts food.upcase
puts food.reverse
